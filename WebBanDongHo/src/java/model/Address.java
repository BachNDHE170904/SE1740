package model;

/**
 *
 * @author ADMIN
 */
public class Address {
    private String username,firstname,lastname,phone,address;

    public Address() {
    }

    public Address(String username, String firstname, String lastname, String phone, String address) {
        this.username = username;
        this.firstname = firstname;
        this.lastname = lastname;
        this.phone = phone;
        this.address = address;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getFirstname() {
        return firstname;
    }

    public void setFirstname(String firstname) {
        this.firstname = firstname;
    }

    public String getLastname() {
        return lastname;
    }

    public void setLastname(String lastname) {
        this.lastname = lastname;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    @Override
    public String toString() {
        return "Address{" + "username=" + username + ", firstname=" + firstname + ", lastname=" + lastname + ", phone=" + phone + ", address=" + address + '}';
    }
    
}
